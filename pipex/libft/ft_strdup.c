#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;
	int		i;

	i = 0;
	result = (char *) malloc(ft_strlen(s1) + 1);
	if (result == 0 || s1 == 0)
	{
		result = 0;
		return (result);
	}
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
